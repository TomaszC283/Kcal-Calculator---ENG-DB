package TomaszC283.main.java;

public class Products {
	
	private int ProductID;
	private String ProductName;
	private double ProductCarbo;
	private double ProductWhey;
	private double ProductFats;
	
	public int getProductID() {
		return ProductID;
	}
	public void setProductID(int productID) {
		ProductID = productID;
	}
	public String getProductName() {
		return ProductName;
	}
	public void setProductName(String productName) {
		ProductName = productName;
	}
	public double getProductCarbo() {
		return ProductCarbo;
	}
	public void setProductCarbo(double productCarbo) {
		ProductCarbo = productCarbo;
	}
	public double getProductWhey() {
		return ProductWhey;
	}
	public void setProductWhey(double productWhey) {
		ProductWhey = productWhey;
	}
	public double getProductFats() {
		return ProductFats;
	}
	public void setProductFats(double productFats) {
		ProductFats = productFats;
	}


}
